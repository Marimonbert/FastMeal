package com.example.fastmeal.modelo;

public class AvaliacaoGarcom {

    private String uidAvaliacao;
    private String idGarcom;
    private Float nota;
    private String nomeGarcom;

    public String getUidAvaliacao() {
        return uidAvaliacao;
    }

    public void setUidAvaliacao(String uidAvaliacao) {
        this.uidAvaliacao = uidAvaliacao;
    }

    public String getIdGarcom() {
        return idGarcom;
    }

    public void setIdGarcom(String idGarcom) {
        this.idGarcom = idGarcom;
    }

    public Float getNota() {
        return nota;
    }

    public void setNota(Float nota) {
        this.nota = nota;
    }

    public String getNomeGarcom() {
        return nomeGarcom;
    }

    public void setNomeGarcom(String nomeGarcom) {
        this.nomeGarcom = nomeGarcom;
    }
}
